package MyMath;
public class Arithmatic
 {
  float a,b;
  public void initialize(float temp1,float temp2)
   {
    a = temp1;
    b = temp2;
   }
  public float addition()
   {
    return a + b;
   }
  public float substraction()
   {
    return a - b;
   }
  public float multiplication()
   {
    return a * b;
   }
  public float division()
   {
    return b / a;
   }
 }